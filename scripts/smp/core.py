def table_html(headers: list, rows: list[str], table_id: str | None = None, empty_message: str = "No data yet.", wrap_cls: str = "", caption: str | None = None, pos_filter: bool = False, colgroups: Any = None, default_colgroup: str | None = None, sticky_year: bool | None = None) -> str:
    """Sortable table shell.

    Optional extras (all default-off, so existing call sites render unchanged):
      colgroups: ordered mapping of group label -> column indices (dict or list of
        tuples). Emits data-colgroup on the matching th/td cells plus a segmented
        control that shows one group at a time (+ All); the choice persists per
        table id in localStorage. Columns in no group are always visible.
      default_colgroup: group label preselected by the control (before any stored
        choice). The server always renders every column; JS applies the group.
      sticky_year: pin the second column next to the sticky first column. When
        None it turns on automatically for Season/Year + Player/Team/Name pairs.
    """
    table_id_attr = f' id="{esc(table_id)}"' if table_id else ""
    if not rows:
        return f'<p class="empty-state">{esc(empty_message)}</p>'
    labels = [label if isinstance(label, str) else label[0] for label in headers]
    header_html = "".join(th(label) if isinstance(label, str) else th(label[0], label[1]) for label in headers)
    body_rows = [row if row.lstrip().startswith("<tr") else f"<tr>{row}</tr>" for row in rows]
    wrap_cls_attr = f" {wrap_cls}" if wrap_cls else ""
    caption_text = caption or (table_id.replace("-", " ").title() if table_id else "")
    caption_html = f'<caption class="sr-only">{esc(caption_text)}</caption>' if caption_text else ""
    # Optional position filter: only when a "Pos" column exists and the table has an id (for JS wiring).
    pos_bar, pos_attr = "", ""
    if pos_filter and table_id:
        if "Pos" in labels:
            pos_attr = f' data-pos-col="{labels.index("Pos")}"'
            options = "".join(f'<option value="{esc(v)}">{esc(l)}</option>' for v, l in POS_FILTER_OPTIONS)
            pos_bar = (f'<div class="toolbar pos-filter-bar"><label class="select-label">Position '
                       f'<select data-pos-filter="{esc(table_id)}">{options}</select></label></div>')
    # Optional column groups: annotate cells + emit the segmented control.
    colgroup_bar = ""
    if colgroups and table_id:
        groups = list(colgroups.items()) if isinstance(colgroups, dict) else list(colgroups)
        tokens_by_col: dict[int, list[str]] = {}
        for group_label, cols in groups:
            token = slugify(str(group_label), "group")
            for col in cols:
                tokens_by_col.setdefault(safe_int(col), []).append(token)
        token_attr = {col: " ".join(tokens) for col, tokens in sorted(tokens_by_col.items())}
        header_html = _annotate_colgroup_cells(header_html, token_attr, "th")
        body_rows = [_annotate_colgroup_cells(row, token_attr, "td") for row in body_rows]
        valid_tokens = {slugify(str(g), "group") for g, _ in groups}
        default_token = slugify(str(default_colgroup), "group") if default_colgroup else "all"
        if default_token not in valid_tokens:
            default_token = "all"
        buttons = []
        for label_text, token in [(str(g), slugify(str(g), "group")) for g, _ in groups] + [("All", "all")]:
            active = " active" if token == default_token else ""
            pressed = "true" if token == default_token else "false"
            buttons.append(
                f'<button type="button" class="cg-btn{active}" data-colgroup="{esc(token)}" '
                f'aria-pressed="{pressed}">{esc(label_text)}</button>'
            )
        colgroup_bar = (
            f'<div class="view-toggle colgroup-toggle" data-colgroup-toggle="{esc(table_id)}" '
            f'data-colgroup-default="{esc(default_token)}" role="group" aria-label="Column groups">'
            f'{"".join(buttons)}</div>'
        )
    # Sticky Year column: auto-on when the first two columns are a Year/Name pair.
    if sticky_year is None:
        sticky_year = len(labels) >= 3 and (
            (labels[0] in _STICKY_YEAR_LABELS and labels[1] in _STICKY_NAME_LABELS)
            or (labels[0] in _STICKY_NAME_LABELS and labels[1] in _STICKY_YEAR_LABELS)
        )
    table_cls = ' class="sticky-col2"' if sticky_year else ""
    body_html = "\n".join(body_rows)
    return f"""
    {pos_bar}
    {colgroup_bar}
    <div class="table-wrap{wrap_cls_attr}">
      <table{table_id_attr}{pos_attr}{table_cls} data-sortable>
        {caption_html}
        <thead><tr>{header_html}</tr></thead>
        <tbody>
          {body_html}
        </tbody>
      </table>
    </div>
    """


