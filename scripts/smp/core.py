_CELL_OPEN_RE = {tag: re.compile("<" + tag + r"(?=[\s>])") for tag in ("td", "th")}


def _annotate_colgroup_cells(row_html: str, token_by_col: dict[int, str], tag: str) -> str:
    """Add data-colgroup="…" to the Nth <td>/<th> open tag per token_by_col."""
    counter = [-1]

    def repl(match: re.Match) -> str:
        counter[0] += 1
        token = token_by_col.get(counter[0])
        return f'<{tag} data-colgroup="{token}"' if token else match.group(0)

    return _CELL_OPEN_RE[tag].sub(repl, row_html)


